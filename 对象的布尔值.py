# 所有对象都有一个bool值
# 以下布尔值都是False
# Flase
print('----------以下对象的布尔值为False，其他对象的bool值均为True----------')
print(bool(False))
# 0
print(bool(0))
# None
print(bool(None))
# 空字符串
print(bool(''))# 长度为0的字符串
# 空列表
print(bool([]))# 空列表
print(bool(list())) # 空列表
# 空元组
print(bool(()))# 空元组
print(bool(tuple()))# 空元组
# 空字典
print(bool({}))# 空字典
print(bool(dict()))# 空字典
# 空集合
print(bool(set()))# 空集合