# 转义功能首字母
# \n换行是new line
# \r是return
# backspace 也就是\b
# \t也就是tab
print('hello\nworld')
print('hello\tworld')
print('helloooooo\tworld')
print('hello\rworld')
print('hello\bworld')  # \b是一个推格
# 网址的解决办法
print("http:\\\\www.baidu.com")
print("老师说：\'大家好\'")

# 原字符
print(r"老师说：我是你爸爸'哈哈哈哈'")
# 但是原字符最后面不能是\
